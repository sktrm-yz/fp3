#!/bin/bash

# 各項目のタイトルを配列に格納
titles=(
    "ファイナンシャル・プランニングと倫理"
    "ファイナンシャル・プランニングと関連法規"
    "ライフプランニングの考え方・手法"
    "社会保険"
    "公的年金"
    "企業年金・個人年金等"
    "年金と税金"
    "ライフプラン策定上の資金計画"
    "ローンとカード"
    "ライフプランニングと資金計画の最新の動向"
)

# 各ファイルを作成
for i in "${!titles[@]}"; do
    filename="$((i + 1))_${titles[i]}.md"
    echo "# ${titles[i]}" > "$filename"
    echo "" >> "$filename"
    echo "このファイルは「${titles[i]}」に関する内容です。" >> "$filename"
done