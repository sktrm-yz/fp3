#!/bin/bash

# 各項目のタイトルを配列に格納
titles=(
    "01_ファイナンシャル・プランニングと倫理"
    "02_ファイナンシャル・プランニングと関連法規"
    "03_ライフプランニングの考え方・手法"
    "04_社会保険"
    "05_公的年金"
    "06_企業年金・個人年金等"
    "07_年金と税金"
    "08_ライフプラン策定上の資金計画"
    "09_ローンとカード"
    "10_ライフプランニングと資金計画の最新の動向"
)

# 各ファイルを作成
for i in "${!titles[@]}"; do
    filename="$(printf "%02d" $((i + 1)))_${titles[i]}.md"
    echo "# ${titles[i]}" > "$filename"
    echo "" >> "$filename"
    echo "このファイルは「${titles[i]}」に関する内容です。" >> "$filename"
done